// Flow Control 

// For loops 
var fruits= ['apples', 'oranges', 'grapes', 'lemons'];

for(var i = 0; i < fruits.length; 1 ++){
    console.log(fruits[i]);
}

// If -Else 

var number = 7;

if (number % 2 == 0)   {
    console.log(number + "is even");
} else{
    console.log (number + 'is odd.');
}